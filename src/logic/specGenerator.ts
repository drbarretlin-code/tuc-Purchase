import type { AIHint } from './aiEnhancer';

export interface SpecData {
  department: string;
  requester: string;
  equipmentName: string;
  model?: string;
  category: string;
  requirement: string;
  aiHints: AIHint[];
}

export const generateMarkdown = (data: SpecData): string => {
  const { department, requester, equipmentName, model, category, requirement, aiHints } = data;
  
  const envHint = aiHints.find(h => h.section === 'environmental');
  const regHint = aiHints.find(h => h.section === 'regulations');
  const safetyHint = aiHints.find(h => h.section === 'safety');
  const acceptanceHint = aiHints.find(h => h.section === 'acceptance');

  return `<div class="doc-header">
# 台燿科技股份有限公司
## Taiwan Union Technology Corporation
## 請購驗收規範表 (右側顯示：頁數：X/Y)
</div>

### 一、 名稱
${equipmentName} ${model || ''} (${category})

### 二、 申請單位
${department}

### 三、 人員/分機
${requester}

### 四、 工程適用範圍
${equipmentName}

### 五、 工程適用區間
${equipmentName} 所在位置周遭區域

### 六、 設計要求
1. 供應商需負責設備之搬運、定位與安裝。
2. 施工需符合台燿廠內機電標準。
3. **AI 建議參考 [環保要求]**：
   - ${envHint?.content || '載入中...'}
   - [參考連結](${envHint?.link || '#'})
4. **AI 建議參考 [相關法規]**：
   - ${regHint?.content || '載入中...'}
   - [參考連結](${regHint?.link || '#'})

### 七、 安全要求
1. 設計與安裝符合職業安全衛生法令規範。
2. **AI 建議參考 [職安衛細則]**：
   - ${safetyHint?.content || '載入中...'}
   - [參考連結](${safetyHint?.link || '#'})

### 八、 需求說明
${requirement}

### 九、 安裝程序
#### 施工標準：
1. 使用金屬零件使用白鐵亮面材質。
2. PLC 以及人機程式修改，施工當天即提供修改前後程式備份。
3. PLC 程式修改，原則保留原始程式，另新增輔助接點作新舊功能切替使用，達到可快速復原功能。
4. 工程依職業安全衛生法令規範作業。
5. 工程固定安全衛生管理監工乙員，現場無監工不得作業。
6. 工程完工符合人員安全操作及維修方便性，承包商自行評估進來。
7. 本案工程以功能符合需求、責任施工，承包商不得追加費用。
8. 工程完工設施整體須整齊、美觀、安全。未經核准不得使用葫蘆吊。
9. 手閥把手顏色依操作方式裝設。常開(藍色)/常閉(紅色)/調整(黃色)。
10. 金屬管、支架屬 sus 亮面材質。
11. 現場施工需維護環境及設備整潔。
12. 工程施工每一階段會監工人員查看拍照，未會同造成後續驗收作業問題，承包商自行承擔。
13. 工程施工進行符合職業安全衛生法及 tuc 協力廠商環安衛管理規則，相關作業費用已包含於工程內。
14. 施工日期須配合業主許可日期進行。
15. 本工程金屬類廢棄物須清運至 tuc 指定位置，其餘非金屬類廢棄物承包商須負責處理。
16. 承包工程當訂單確定後須提供進廠相關資料，如附件內容。

#### 驗收：
- 完工後會同勘查 1 個月後驗收。
- **AI 建議參考 [驗收重點]**：
  - ${acceptanceHint?.content || '載入中...'}
  - [參考連結](${acceptanceHint?.link || '#'})

### 十、 遵守事項
1. 工程設施驗收後保固一年，工程費用含萬分之 7 工程保險。
2. 施工配合本公司安排日期((不含例假日))，施工期間遇有臨時問題需停工配合本公司安排。
3. 有動火,高架,吊掛,危險管路，等特殊作業須作業表單流程核准後方可施工，作業時需有 tuc 監工人員在場，tuc 監工人員未在場施作特殊作業，依規定罰款。
4. 施工區域有安全疑慮時，圍出管制作業區域，防止人員車輛進入。
5. 液體管路拆卸須使用盛盤承接，造成危害及損壞依價賠償。
6. 危害性化學物質作業，須配戴個人防護器具。
7. 廠區禁攜帶打火機、砂輪切割機(廠內現場)，廠區內切割使用線鋸機。
8. 施工區域標示及警示(以警示帶或圍籬)，區分隔開，夜間裝警示閃光燈。
9. 廠內天花板動手過需擦拭乾淨或更新。
10. 危險管路拆卸備護目鏡(罩)、膠手套、防護衣、氣密式防毒口罩。
11. 施工臨時電線跨走道地面，使用斑馬膠帶黏貼警示。
12. 施工當日收工清潔環境並將垃圾載走，違反開單罰款。
13. 施工人員進廠全程戴工程帽作業，禁止外籍勞工入廠作業(有內政部核准文件:外籍勞工可移動工作場所，檢覆文件辦理)。
14. 進廠人員不得有宿醉或酒氣情況，違者人員須離廠禁止作業。
15. 屋頂、侷限空間作業須符合職業安全衛生設施規則，且於施工前 3 日提出作業申請(附上作業方法及防護器具清冊文件，相關作業儀器須定期第三方檢測合格標章)。
16. 電力/電控盤與旋轉/傳動機構張貼警告標語。
17. 因本工程造成設施(備)損(傷)壞須賠償。

### 十二、 驗收要求表格
| 類別 | 項目 | 規格要求 | 測試方法 | 樣品數 | 確認 |
| :--- | :--- | :--- | :--- | :--- | :--- |
| 功能 | 基本運轉 | 依設備說明書 | 現場測試 | 全部 | NA |
| 品質 | 外觀檢驗 | 無傷痕/鏽蝕 | 目視檢驗 | 全部 | NA |
| 產能 | 額定出力 | 符合本規範 | 運轉紀錄 | 全部 | NA |
| 其他 | 文件交付 | 操作手冊 | 點收確認 | 2 份 | NA |
| 空白 | NA | NA | NA | NA | NA |
| 空白 | NA | NA | NA | NA | NA |
`;
};
